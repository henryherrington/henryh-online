            <h1>The Origin of Hello Robin</h1>
          
            <div id="origins">
              
              <p>It all started in 2013 with a text message from Molly Moon’s husband Zack to Robin Wehl Martin.</p>
              
              <div id="textconvo">
                <div class="bluemessage">
                  Hey Robin, it's zack.  I need a small favor, if you don't mind.
                </div><br>
                <div class="whitemessage">
                  Sure.
                </div><br>
                <div class="bluemessage">
                  There is a small space left in the new building on 19th. I need you to open a bakery there. Agreed?
                </div><br>
                <div class="whitemessage">
                  Omg!  Yes!!  Does it also sell ice cream?
                </div><br>
                <div class="bluemessage">
                  Molly’s into it if you are.
                </div>
              </div>
              
              <p>And more than six years later Hello Robin is still using Molly Moon's ice cream for their ice cream sandwiches.</p>
              
              <p>In the years prior to this, Robin had built a reputation on Capitol Hill for delicious baked goods. She won Best Classic Whoopie Pie two years running at the Oddfellows Whoopie Pie Bake-off. (The next year they gave up and made her a judge.) She won top honors for “Overall Awesomeness” at the City Arts Pie Life competition and 1st place at the Crossroads Farmer’s Market bake off and had done baking stints at various Seattle restaurants. And that's when Robin's baked goods got on Molly's radar.</p>

              <p>It was the cookies that first got Molly’s attention. Molly and Zack had given Robin and Clay (her husband) and three kids their chickens (Faye, Ida, Bambi and Levina) and as a thank you, Robin used the eggs to make delicious cookies she would occasionally leave on Molly and Zack's doorstep. Molly and Zack became addicted and realized they needed to strategize a way to gain access to those baked goods on a daily basis. Hence, the text message that started this story.</p>
              
               <p>At this same time, the demand for Robin’s cookies for school fund-raisers, bar mitzvahs and neighbors with an overly active sweet tooth had become too much for one residential kitchen and with the nudge from Molly, Robin and Clay gave in to popular demand and made the leap from home kitchen to retail storefront on the east side of Capitol Hill at the end of 2013.</p>
              
